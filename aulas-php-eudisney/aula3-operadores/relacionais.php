# Operadores Aritméticos: + - / * %
// Relacionais: >, <, >=, <=, ==, !=
/* Lógicos: && (e),||(ou),! (não) */ 
# Incremento: ++, +=, --, -=

$res1 = 10 > 20; # Maior
$res2 = 10 < 20; # Menor
$res3 = 10 != 20; # Diferente
$res4 = 10 == 20; # Igual
$res5 = 10 >= 20; # Maio ou Igual
$res6 = 10 <= 20; # Menor ou Igual

var_dump($res1);
var_dump($res2);
var_dump($res3);
var_dump($res4);
var_dump($res5);
var_dump($res6);

